// tipos de funcao

// função declarativa
// necessitam de um nome

function funcaoDeclarativa() {
    console.log('Sou uma função declarativa');
}

funcaoDeclarativa();

// expressão de função
// com nomeação

const funcaoExpressao = function() {
    console.log('Sou uma função expressão');
}

funcaoExpressao();

// arrow function

var funcao = () => {
    console.log('Sou uma arrow function');
}

funcao();

