// > Tipos primitivos

// Booleanos
let vOuF = false;
console.log(typeof vOuF);

// Números
let number = 10;
console.log(typeof number);

// Strings
let nome = "Antonio";
console.log(typeof nome);

// > Tipos de variáveis

// var - variável global que pode ser acessada em qualquer lugar do código

// let - variável local que só pode ser acessada dentro do escopo onde foi criada

// const - variável local que não pode ser alterada



// > Atribuição

// = - atribui um valor a uma variável

// > Comparação

// == - compara se os valores são iguais

console.log(10 == '10'); // true

// === - compara se os valores e os tipos são iguais

console.log(10 === 1); // false

// > Operadores aritméticos

// + - soma
// - - subtração
// * - multiplicação
// / - divisão
// % - resto da divisão
// ** - potência

console.log(2 ** 2);

// > Operadores relacionais

// > - maior que
// < - menor que
// >= - maior ou igual que
// <= - menor ou igual que
// == - igualdade
// != - diferente

console.log(10 > 5); // true

// > Operadores lógicos

// && - e
// || - ou
// ! - negação

let booleanValue = true;

console.log(booleanValue && 10 > 20); // false
